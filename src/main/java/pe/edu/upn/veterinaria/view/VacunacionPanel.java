    private void cargarVacunas() {

    cboVacuna.removeAllItems();

    List<VacunaDTO> lista =
            controller.listarVacunas();

    for (VacunaDTO v : lista) {

        cboVacuna.addItem(
                v.getIdVacuna()
                + " - "
                + v.getNombre());

    }

}
    private int obtenerIdCombo(
        String texto) {

    return Integer.parseInt(
            texto.split("-")[0].trim());

}
    private void cargarMascotas() {

    cboMascota.removeAllItems();

    List<MascotaDTO> lista =
            controller.obtenerMascotas();

    for (MascotaDTO m : lista) {

        cboMascota.addItem(
                m.getIdMascota()
                + " - "
                + m.getNombre());

    }

}
    private void cargarVacunaciones() {

    DefaultTableModel modelo =
            (DefaultTableModel)
                    tblVacunaciones.getModel();

    modelo.setRowCount(0);

    List<VacunacionConsultaDTO> lista =
            controller.consultarVacunaciones();

    for (VacunacionConsultaDTO v : lista) {

        modelo.addRow(
                new Object[]{
                        v.getIdVacunacion(),
                        v.getFechaAplicacion(),
                        v.getMascota(),
                        v.getVacuna()
                });

    }

}
    private void limpiar() {

    txtFechaAplicacion.setText("");

    if (cboMascota.getItemCount() > 0)
        cboMascota.setSelectedIndex(0);

    if (cboVacuna.getItemCount() > 0)
        cboVacuna.setSelectedIndex(0);

}

