    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {

        java.util.Date fecha =
                java.sql.Date.valueOf(
                        txtFechaAplicacion
                                .getText()
                                .trim());

        int idMascota =
                obtenerIdCombo(
                        cboMascota
                                .getSelectedItem()
                                .toString());

        int idVacuna =
                obtenerIdCombo(
                        cboVacuna
                                .getSelectedItem()
                                .toString());

        controller.registrarVacunacion(
                fecha,
                idMascota,
                idVacuna);

        JOptionPane.showMessageDialog(
                this,
                "Vacunación registrada correctamente");

        cargarVacunaciones();

        limpiar();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }

    }//GEN-LAST:event_btnRegistrarActionPerformed


