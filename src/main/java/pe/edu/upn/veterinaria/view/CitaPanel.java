    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
        try {

        java.sql.Date fecha = java.sql.Date.valueOf(txtFechaCita.getText().trim());

        String horaTexto =
                txtHora.getText().trim();

        String motivo =
                txtMotivo.getText().trim();

        String estado =
                cboEstado.getSelectedItem()
                        .toString();

        int idMascota =
                obtenerIdCombo(
                        cboMascota
                                .getSelectedItem()
                                .toString());

        int idEmpleado =
                controller
                        .getEmpleadoLogueado()
                        .getIdEmpleado();

        java.sql.Time hora =
                java.sql.Time.valueOf(
                        horaTexto + ":00");

        controller.registrarCita(
                fecha,
                hora,
                motivo,
                estado,
                idMascota,
                idEmpleado);

        JOptionPane.showMessageDialog(
                this,
                "Cita registrada correctamente");

        cargarCitas();

        limpiar();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }
    }//GEN-LAST:event_btnRegistrarActionPerformed


