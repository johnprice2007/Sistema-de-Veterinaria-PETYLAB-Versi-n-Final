    private void cargarMascotas() {

    cboMascota.removeAllItems();

    List<MascotaDTO> lista =
            controller.obtenerMascotas();

    for (MascotaDTO m : lista) {

        cboMascota.addItem(
                m.getIdMascota()
                + " - "
                + m.getNombre());

    }

}
   private void cargarEstados() {

    cboEstado.removeAllItems();

    cboEstado.addItem("Programada");
    cboEstado.addItem("Atendida");
    cboEstado.addItem("Cancelada");

} 
   private int obtenerIdCombo(
        String texto) {

    return Integer.parseInt(
            texto.split("-")[0].trim());

}
   private void cargarCitas() {

    DefaultTableModel modelo =
            (DefaultTableModel)
                    tblCitas.getModel();

    modelo.setRowCount(0);

    List<CitaConsultaDTO> lista =
            controller.consultarCitas();

    for (CitaConsultaDTO c : lista) {

        modelo.addRow(
                new Object[]{
                        c.getIdCita(),
                        c.getFechaCita(),
                        c.getHoraCita(),
                        c.getMascota(),
                        c.getEmpleado(),
                        c.getMotivo(),
                        c.getEstado()
                });

    }

}
   private void limpiar() {

    txtHora.setText("");

    txtMotivo.setText("");

    if (cboMascota.getItemCount() > 0)
        cboMascota.setSelectedIndex(0);

    if (cboEstado.getItemCount() > 0)
        cboEstado.setSelectedIndex(0);

}
   

