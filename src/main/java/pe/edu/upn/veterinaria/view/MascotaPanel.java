    private void cargarClientes() {

    cboCliente.removeAllItems();

    List<ClienteDTO> lista =
            controller.listarClientes();

    for (ClienteDTO c : lista) {

        cboCliente.addItem(
                c.getIdCliente() +
                " - " +
                c.getNombre());
    }
}
    private void cargarRazas() {

    cboRaza.removeAllItems();

    List<RazaDTO> lista =
            controller.listarRazas();

    for (RazaDTO r : lista) {

        cboRaza.addItem(
                r.getIdRaza() +
                " - " +
                r.getNombreRaza());
    }
}
    private void cargarSexos() {

    cboSexo.removeAllItems();

    List<SexoDTO> lista =
            controller.listarSexos();

    for (SexoDTO s : lista) {

        cboSexo.addItem(
                s.getIdSexo() +
                " - " +
                s.getDescripcion());
    }
}
    private void cargarColores() {

    cboColor.removeAllItems();

    List<ColorDTO> lista =
            controller.listarColores();

    for (ColorDTO c : lista) {

        cboColor.addItem(
                c.getIdColor() +
                " - " +
                c.getDescripcion());
    }
}
    private void cargarUbicaciones() {

    cboUbicacion.removeAllItems();

    List<UbicacionDTO> lista =
            controller.listarUbicaciones();

    for (UbicacionDTO u : lista) {

        cboUbicacion.addItem(
                u.getIdUbicacion() +
                " - " +
                u.getDepartamento());
    }
}
    private void cargarMascotas() {

    DefaultTableModel modelo =
            (DefaultTableModel)
            tblMascotas.getModel();

    modelo.setRowCount(0);

    List<MascotaConsultaDTO> lista =
            controller.consultarMascotas();

    for (MascotaConsultaDTO m : lista) {

        modelo.addRow(new Object[] {

             m.getIdMascota(),
             m.getNombreMascota(),
             m.getFechaNacimiento(),
             m.getCliente(),
             m.getRaza(),
             m.getSexo(),
             m.getColor(),
             m.getUbicacion()

        });
    }
}
    private int obtenerIdCombo(String texto) {

    return Integer.parseInt(
            texto.split("-")[0].trim());

}
    private void limpiar() {

    txtNombreMascota.setText("");
    txtFechaNacimiento.setText("");

    if (cboCliente.getItemCount() > 0)
        cboCliente.setSelectedIndex(0);

    if (cboRaza.getItemCount() > 0)
        cboRaza.setSelectedIndex(0);

    if (cboSexo.getItemCount() > 0)
        cboSexo.setSelectedIndex(0);

    if (cboColor.getItemCount() > 0)
        cboColor.setSelectedIndex(0);

    if (cboUbicacion.getItemCount() > 0)
        cboUbicacion.setSelectedIndex(0);

    txtNombreMascota.requestFocus();
}

