    private void btnRegistrarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnRegistrarActionPerformed
         System.out.println("🔥 BOTON REGISTRAR PRESIONADO");
        try {
   
        String nombre =
                txtNombreMascota
                        .getText()
                        .trim();

        String fechaTexto =
                txtFechaNacimiento
                        .getText()
                        .trim();

        if (nombre.isEmpty()) {

            JOptionPane.showMessageDialog(
                    this,
                    "Ingrese el nombre de la mascota");

            return;
        }

        java.util.Date fecha =
                java.sql.Date.valueOf(
                        fechaTexto);

        int idCliente =
                obtenerIdCombo(
                        cboCliente
                                .getSelectedItem()
                                .toString());

        int idRaza =
                obtenerIdCombo(
                        cboRaza
                                .getSelectedItem()
                                .toString());

        int idSexo =
                obtenerIdCombo(
                        cboSexo
                                .getSelectedItem()
                                .toString());

        int idColor =
                obtenerIdCombo(
                        cboColor
                                .getSelectedItem()
                                .toString());

        int idUbicacion =
                obtenerIdCombo(
                        cboUbicacion
                                .getSelectedItem()
                                .toString());

        controller.registrarMascota(
                nombre,
                fecha,
                idCliente,
                idRaza,
                idSexo,
                idColor,
                idUbicacion);

        JOptionPane.showMessageDialog(
                this,
                "Mascota registrada correctamente");

        cargarMascotas();

        limpiar();

    } catch (Exception e) {

        JOptionPane.showMessageDialog(
                this,
                e.getMessage());

    }
    }//GEN-LAST:event_btnRegistrarActionPerformed

